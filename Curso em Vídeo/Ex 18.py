#Sin, Cossin, Tangent

import math

angule = float(input('Digite um ângulo:'))

print('O seno de {} é {:.2f}'.format(angule,math.sin(math.radians(angule))))
print('O cosseno de {} é {:.2f}'.format(angule,math.cos(math.radians(angule))))
print('O tangente de {} é {:.2f}'.format(angule,math.tan(math.radians(angule))))
